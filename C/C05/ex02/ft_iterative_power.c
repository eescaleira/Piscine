int	ft_iterative_power(int nb, int power)
{
	int	i;
	int	n;

	i = 1;
	n = nb;
	if (power < 0)
		return (0);
	if (power == 0)
		return (1);
	while (++i <= power)
	{
		nb *= n;
	}
	return (nb);
}

int		main(void)
{
	int		nb;
	int		power;

	nb = 5;
	power = 4;
	printf("%d\n", ft_iterative_power(nb, power));
}